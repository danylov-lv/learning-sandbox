def top_n_per_source(spark, events_df, n: int = 3):
    """Top n offers per source_id, ranked by price, http_status==200 rows only.

    This is the leaderboard PriceWatch's product managers want: "for each
    source, what are its n highest-priced live offers right now, and in
    what order?"

    Contract, matched exactly against `ground-truth.json`'s
    `top_n_per_source.by_source`:
      - Filter to `http_status == 200` first (price is only meaningful on a
        successful scrape).
      - Order by `price` descending, breaking ties by `product_id`
        descending — this is a total order over (price, product_id), so
        "ties" only ever occur when both columns are equal (the same
        product recorded at the same price on two different snapshots,
        which whole-row `.distinct()` does not collapse since the rows
        differ in `captured_at` or other columns).
      - Take the first n rows of that order, per source_id.

    You must use a window function here (partitionBy("source_id"),
    orderBy(desc("price"), desc("product_id"))) — the validator inspects
    the plan and requires a `Window` node. The one thing worth getting
    right before you write any code: which ranking function.

    `row_number()`, `rank()`, and `dense_rank()` all answer different
    questions once there's a tie on the full (price, product_id) order key,
    and only one of them reproduces `ground-truth.json` exactly:
      - `ground-truth.json` was generated by literally sorting each
        source's deduplicated http_status==200 rows by (price desc,
        product_id desc) and slicing the first n — every row counts once
        toward that slice, tied or not, and the slice always has exactly n
        entries (as long as the source has >= n rows at all).
      - `rank()` and `dense_rank()` both let every row tied for a boundary
        position "through" — if 3 rows are tied for the #3 spot, a
        `rank() <= 3` filter returns more than 3 rows for that source. That
        does not match `ground-truth.json`, which always caps at exactly n.
      - `row_number()` assigns strictly increasing integers even to tied
        rows, so `row_number() <= n` always returns exactly n rows per
        source (given >= n rows to begin with) — this is what
        `ground-truth.json`'s slicing actually does, and it's the function
        that reproduces the ground truth exactly, including sources where
        the same product_id appears twice in the top n at two different
        prices (that's not a tie at all, just two distinct top-n rows for
        the same product — verify this against a source's list in
        `ground-truth.json` before you trust your output).

    Returns:
        a DataFrame with columns (source_id, product_id, price, rn), one
        row per (source_id, rn) pair for rn in [1, n], where rn=1 is that
        source's single highest-priced offer. Do not filter to distinct
        product_id — the same product may legitimately appear more than
        once in one source's top n at different prices.
    """
