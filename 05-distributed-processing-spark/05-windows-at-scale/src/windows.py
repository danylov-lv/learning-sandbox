"""Window functions over PriceWatch's price-snapshot events, at a scale where
the naive approach falls over.

PriceWatch wants two leaderboards:
  - the top-N offers per source (a per-source leaderboard by price), and
  - per-product price movement over time (latest price vs. the previous
    snapshot).

Both are naturally window questions: "rank within a group" and "compare a
row to another row in the same group's ordering." At 2M rows this is
comfortable no matter how you write it. At 50M+ rows, with source_id 1
holding roughly 30% of all traffic, the difference between a window whose
partitionBy key is that skewed and a pre-aggregated equivalent stops being
academic — one huge partition has to be sorted end to end by a single task
while the other 19 finish in a fraction of the time.

Raw JSONL rows have a top-level `_corrupt_record` column when a line fails
to parse as JSON — "drop unparseable lines" always means
`filter(col("_corrupt_record").isNull())`.

"Deduplicate" always means whole-row `.distinct()` on the parsed events
(after dropping `_corrupt_record`), removing the retry-storm repeats
injected during generation.
"""

