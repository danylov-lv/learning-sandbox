def price_change_per_product(spark, events_df):
    """Per-product latest price vs. its previous snapshot, via lag().

    PriceWatch's other leaderboard: "for every product, what did its price
    just do?" Build this with `lag()` over
    `partitionBy("product_id").orderBy("captured_at")` on http_status==200
    rows (price is only meaningful when the scrape succeeded):

      1. Filter events_df to http_status == 200.
      2. `prev_price = lag("price").over(w)` and
         `prev_captured_at = lag("captured_at").over(w)`, where `w` is
         partitioned by product_id and ordered by captured_at ascending —
         this gives every row the price and timestamp of the *previous*
         observation for that same product, or null if it's the product's
         first observation.
      3. Keep only the last observation per product (the one with the
         maximum captured_at in its partition) — that row's `price` is the
         product's current/latest price, and its `prev_price` /
         `prev_captured_at` (computed in step 2, before this filter) are
         exactly the previous snapshot's values.
      4. Compute `price_delta = price - prev_price` (null propagates
         naturally when prev_price is null, i.e. a product with only one
         valid observation).

    This dataset's captured_at timestamps are effectively unique per
    product among http_status==200 rows (verify this assumption on your
    own machine — two valid snapshots of the same product at the exact
    same second would make "the previous row" ambiguous, and both this
    function and the validator's independent reference need to agree on
    which row that is).

    The validator does not compare this function's output to
    `ground-truth.json` (there is no lag-based aggregate in it) — instead
    it builds its own reference for the same quantity using only
    `groupBy`/`join` (no window function at all) and checks that your
    window-based answer and its groupBy/join answer agree on every
    product. That agreement, not a fixed expected value, is the point:
    a window and a hand-rolled "find the max, then find the max of what's
    left" pipeline are two ways of computing the same thing, and they had
    better produce the same numbers.

    Returns:
        a DataFrame with columns (product_id, latest_price,
        latest_captured_at, prev_price, prev_captured_at, price_delta),
        one row per product_id that has at least one http_status==200
        observation. prev_price/prev_captured_at/price_delta are null for
        a product with exactly one valid observation.
    """
