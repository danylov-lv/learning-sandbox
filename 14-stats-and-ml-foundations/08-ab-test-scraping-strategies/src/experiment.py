"""Fully implemented, deterministic fixture for task 08 -- A/B test scraping
strategies. Do not edit this file; the task is `src/abtest.py`, not this one.

The scenario: a bake-off between two scraping strategies, each attempting to
successfully EXTRACT one required field (e.g. a price) from a batch of
pages.

- **Strategy A** ("plain HTTP"): a single GET, parse, done. Cheap, fast, but
  gives up on anything that needs a retry or a headless render (JS-rendered
  fields, flaky responses, rate-limit hiccups).
- **Strategy B** ("HTTP + retry/headless fallback"): tries the same plain
  GET first, but falls back to a retrying headless-browser render when the
  cheap path fails to find the field. More expensive per page (extra
  latency, extra compute), but recovers some of A's failures.

Each attempt is a Bernoulli trial: success (field extracted) or failure.
`simulate_experiment` draws `n_a` / `n_b` such trials for A and B from FIXED
true success probabilities using `np.random.default_rng(seed)` -- this is
the ground truth the validator uses to sanity-check the fixture itself and
to confirm the "significant" branch fires deterministically at the default
seed. It is NOT something `src/abtest.py` gets to read: the learner's
`two_proportion_test` must work from the observed `a`/`b` arrays alone, the
same way you would from real logged outcomes with no access to the
generating process.

True parameters (population values being simulated, not a result to
recover exactly -- the whole point of the test is that any given sample's
observed proportions will differ from these by sampling noise):

    P_A_TRUE = 0.72   strategy A's true success rate
    P_B_TRUE = 0.78   strategy B's true success rate
    N_A = N_B = 1500  attempts per strategy in the default bake-off

A second, smaller/no-effect scenario (`simulate_null_experiment`) is also
provided for the README's discussion of "not significant != no difference"
and "significant != large effect" -- it is NOT what the validator grades
against; the graded scenario is always the default `simulate_experiment()`.
"""

import numpy as np

P_A_TRUE = 0.72
P_B_TRUE = 0.78
N_A = 1500
N_B = 1500
DEFAULT_SEED = 14095


def simulate_experiment(seed=DEFAULT_SEED, n_a=N_A, n_b=N_B, p_a=P_A_TRUE, p_b=P_B_TRUE):
    """Simulate the bake-off: n_a attempts of strategy A, n_b attempts of
    strategy B, each an independent Bernoulli trial at the given true
    success probability.

    A single `np.random.default_rng(seed)` is consumed in this fixed order:
    all of A's `n_a` trials first, then all of B's `n_b` trials. Changing
    n_a/n_b/p_a/p_b from the defaults changes what gets simulated but never
    the draw order.

    Returns:
        dict with:
          - "a": bool ndarray, shape (n_a,) -- True where strategy A
            successfully extracted the field on that attempt.
          - "b": bool ndarray, shape (n_b,) -- same for strategy B.
          - "n_a", "n_b": int, attempt counts (== len(a)/len(b)).
          - "seed": the seed used.
    """
    rng = np.random.default_rng(seed)
    a = rng.random(n_a) < p_a
    b = rng.random(n_b) < p_b
    return {"a": a, "b": b, "n_a": n_a, "n_b": n_b, "seed": seed}


def simulate_null_experiment(seed=14090, n_a=150, n_b=150, p_a=P_A_TRUE, p_b=P_B_TRUE):
    """A second bake-off scenario, much smaller (150 attempts per strategy
    instead of 1500), same true success probabilities as the default. The
    real gap (p_b - p_a = 0.06) hasn't changed, but a smaller sample means
    more sampling noise relative to the effect -- at this size the gap is
    usually NOT statistically significant, even though the true underlying
    difference is exactly the same 6 points as the graded scenario.

    Not used by the validator. Provided so you can run your own
    `two_proportion_test` against it and see, concretely, that "not
    significant" does not mean "no real difference" -- it can just mean
    "not enough data to tell noise from signal at this sample size."
    """
    rng = np.random.default_rng(seed)
    a = rng.random(n_a) < p_a
    b = rng.random(n_b) < p_b
    return {"a": a, "b": b, "n_a": n_a, "n_b": n_b, "seed": seed}
