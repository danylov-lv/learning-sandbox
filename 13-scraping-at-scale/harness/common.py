"""Shared helpers for module 13 (scraping at scale: a deliberately hostile
local target site, its data-quality/change-detection/selector-resilience/
budget-router pipeline tasks, and Prometheus/Grafana observability of the
whole thing) validators, generators, and task scaffolds.

Every validator prints PASSED or `NOT PASSED: <reason>` and exits 0/1; no
tracebacks reach the learner. Run these host-side via `uv run`. Nothing here
requires a live stack at import time -- httpx and any other third-party
import is lazy inside the functions that actually need them.

There is no shared database in this module -- the target site (Docker,
plain HTTP) is the shared corpus, backed by `data/catalog.json` and
`data/target-spec.json` (both gitignored, regenerated by `generate.py`; only
`data/ground-truth.json` is committed). Parsing/scraping logic is entirely
the learner's own code -- this module deliberately contains NONE.
"""

