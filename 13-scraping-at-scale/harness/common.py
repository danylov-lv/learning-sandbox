def query_prometheus(expr, base_url=None):
    """GET /api/v1/query?query=<expr> against Prometheus -> parsed JSON body,
    or None if Prometheus is unreachable. Raises no exception on connection
    failure (returns None instead) so a caller can decide "skip this check"
    vs "fail" -- Prometheus/Grafana live checks are always skip-if-down in
    this module, never a hard requirement for a task's core pass/fail."""
    import httpx

    url = (base_url or prom_base_url()) + "/api/v1/query"
    try:
        resp = httpx.get(url, params={"query": expr}, timeout=5.0)
        resp.raise_for_status()
        return resp.json()
    except httpx.HTTPError:
        return None
