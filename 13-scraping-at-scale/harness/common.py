# --------------------------------------------------------------------------
# Target client (validators/harness only -- the learner writes their OWN
# fetch layer for the actual scraping tasks)
# --------------------------------------------------------------------------

class TargetClient:
    """Thin httpx.Client wrapper against the target site: sets a browser-like
    default User-Agent/Accept-Language (so it passes the header gate out of
    the box) and a per-instance X-Client-Id (random uuid4 unless given).
    Intentionally minimal -- no retry/backoff/rate-limit awareness, no
    parsing. That is the learner's job in the actual tasks; this exists so
    validators/harness scripts can poke the target without duplicating a
    scraping stack."""

    def __init__(self, client_id=None, base_url=None, headers=None, **httpx_kwargs):
        import httpx

        self.client_id = client_id or str(uuid.uuid4())
        self.base_url = base_url or target_base_url()
        merged_headers = {
            "User-Agent": DEFAULT_USER_AGENT,
            "Accept-Language": DEFAULT_ACCEPT_LANGUAGE,
            "X-Client-Id": self.client_id,
        }
        if headers:
            merged_headers.update(headers)
        self._client = httpx.Client(base_url=self.base_url, headers=merged_headers, **httpx_kwargs)

    def get(self, path, **kwargs):
        return self._client.get(path, **kwargs)

    def close(self):
        self._client.close()

    def __enter__(self):
        return self

    def __exit__(self, *exc):
        self.close()


def get_client_state(client_id, base_url=None):
    """GET /__debug/client for `client_id` -> parsed JSON dict, or NOT PASSED
    if the target is unreachable."""
    import httpx

    url = (base_url or target_base_url()) + "/__debug/client"
    try:
        resp = httpx.get(url, headers={"X-Client-Id": client_id}, timeout=5.0)
        resp.raise_for_status()
        return resp.json()
    except httpx.HTTPError as e:
        not_passed(f"could not reach target /__debug/client on {target_port()}: {e}")


def reset_client(client_id, base_url=None):
    """POST /__debug/reset for `client_id`, or NOT PASSED if unreachable."""
    import httpx

    url = (base_url or target_base_url()) + "/__debug/reset"
    try:
        resp = httpx.post(url, headers={"X-Client-Id": client_id}, timeout=5.0)
        resp.raise_for_status()
        return resp.json()
    except httpx.HTTPError as e:
        not_passed(f"could not reach target /__debug/reset on {target_port()}: {e}")


