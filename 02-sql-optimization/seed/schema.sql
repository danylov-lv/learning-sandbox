    autovacuum_vacuum_scale_factor = 0.8,
    autovacuum_analyze_scale_factor = 0.8
);
-- Defect (g): low statistics target on a skewed column -> planner misestimates.
ALTER TABLE orders ALTER COLUMN status SET STATISTICS 10;

-- ---------------------------------------------------------------------------
-- order_items
-- Defect (b): composite index in the wrong column order for the dominant
--             query pattern (order detail lookups filter by order_id, index
--             leads with product_id); no plain index on order_id at all ->
--             nested-loop pathology when joining orders -> order_items.
-- ---------------------------------------------------------------------------
CREATE TABLE order_items (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id   BIGINT NOT NULL REFERENCES orders (id),
    product_id BIGINT NOT NULL REFERENCES products (id),
    quantity   INTEGER NOT NULL,
    unit_price NUMERIC(12,2) NOT NULL
);

CREATE INDEX idx_order_items_product_order ON order_items (product_id, order_id);
-- Deliberately NO index starting with order_id.

-- ---------------------------------------------------------------------------
