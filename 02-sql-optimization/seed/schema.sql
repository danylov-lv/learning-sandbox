-- ---------------------------------------------------------------------------
-- users
-- ---------------------------------------------------------------------------
CREATE TABLE users (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email       TEXT NOT NULL,
    full_name   TEXT NOT NULL,
    city        TEXT NOT NULL,
    country     TEXT NOT NULL,
    created_at  TIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX idx_users_email ON users (email);

-- ---------------------------------------------------------------------------
-- sellers
-- ---------------------------------------------------------------------------
CREATE TABLE sellers (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        TEXT NOT NULL,
    tier        VARCHAR(10) NOT NULL,
    rating      NUMERIC(3,2) NOT NULL,
    created_at  TIMESTAMP NOT NULL
);

-- ---------------------------------------------------------------------------
-- categories (tree)
-- ---------------------------------------------------------------------------
CREATE TABLE categories (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id   INTEGER REFERENCES categories (id),
    name        TEXT NOT NULL,
    family      VARCHAR(20) NOT NULL,
    depth       SMALLINT NOT NULL
);

CREATE INDEX idx_categories_parent_id ON categories (parent_id);

-- ---------------------------------------------------------------------------
-- products
-- Defect (d): attrs JSONB queried with @> / ->>'brand' but no GIN index.
--             title searched with ILIKE '%term%', no trigram index.
-- ---------------------------------------------------------------------------
CREATE TABLE products (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    seller_id   INTEGER NOT NULL REFERENCES sellers (id),
    category_id INTEGER NOT NULL REFERENCES categories (id),
    title       TEXT NOT NULL,
    price       NUMERIC(12,2) NOT NULL,
    attrs       JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at  TIMESTAMP NOT NULL
);

CREATE INDEX idx_products_seller_id ON products (seller_id);
CREATE INDEX idx_products_category_id ON products (category_id);
-- Deliberately NO GIN index on attrs, NO pg_trgm index on title.

-- ---------------------------------------------------------------------------
-- orders
-- Defect (a): index on (status) alone (low cardinality) but no index on
--             (user_id, created_at) — user order-history queries seq-scan.
-- Defect (h): user_id BIGINT vs users.id INTEGER (cast on join); money as
--             numeric(30,10); status as varchar(10) instead of an enum.
-- Defect (f): autovacuum delayed at table level -> churn phase leaves bloat.
-- ---------------------------------------------------------------------------
CREATE TABLE orders (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id      BIGINT NOT NULL,
    status       VARCHAR(10) NOT NULL,
    total_amount NUMERIC(30,10) NOT NULL,
    created_at   TIMESTAMP NOT NULL
);

CREATE INDEX idx_orders_status ON orders (status);
-- Deliberately NO index on (user_id, created_at).

-- "Temporary" incident mitigation left behind by the previous DBA.
ALTER TABLE orders SET (
