from __future__ import annotations


def apply_discount(price: float, pct: float, *, min_price: float) -> float:
    """Apply a percentage discount, floored at `min_price`.

    - `price` must be >= 0.0, else raises ValueError.
    - `pct` is a percentage and must be within [0.0, 100.0], else raises
      ValueError.
    - Returns `price` reduced by `pct` percent, rounded to 2 decimals, but
      never less than `min_price` (e.g. a store's floor price for a SKU).
    """
    if price < 0.0:
        raise ValueError("price must be >= 0")
    if pct < 0.0 or pct > 100.0:
        raise ValueError("pct must be within [0, 100]")
    discounted = price * (1.0 - pct / 100.0)
    return round(max(discounted, min_price), 2)


def classify_price_tier(price: float) -> str:
    """Classify a price into a display tier.

    - price < 20.0            -> "budget"
    - 20.0 <= price < 100.0   -> "standard"
    - 100.0 <= price < 500.0  -> "premium"
    - price >= 500.0          -> "luxury"
    """
    if price < 20.0:
        return "budget"
    if price < 100.0:
        return "standard"
    if price < 500.0:
        return "premium"
    return "luxury"


def is_valid_sku(sku: str) -> bool:
    """A valid SKU is exactly: 3 uppercase ASCII letters, a literal hyphen,
    then 4 to 6 ASCII digits.

    Examples:
      "ABC-1234"    -> True  (3 letters, 4 digits)
      "ABC-123456"  -> True  (3 letters, 6 digits)
      "abc-1234"    -> False (lowercase letters)
      "AB-1234"     -> False (only 2 letters)
      "ABCD-1234"   -> False (4 letters)
      "ABC-123"     -> False (3 digits, below the 4-digit minimum)
      "ABC-1234567" -> False (7 digits, above the 6-digit maximum)
      "ABC1234"     -> False (missing hyphen)
      "ABC-12-34"   -> False (second hyphen lands in what must be digits)
    """
    if "-" not in sku:
        return False
    letters, _, digits = sku.partition("-")
    if len(letters) != 3 or not letters.isalpha() or not letters.isupper():
        return False
    if not digits.isdigit() or len(digits) < 4 or len(digits) > 6:
        return False
    return True


def shipping_cost(weight_kg: float, *, distance_km: float, express: bool = False) -> float:
    """Compute a shipping charge.

    - Base rate: 2.5 currency units per kg, plus 0.1 per km.
    - `weight_kg` must be > 0.0, else raises ValueError.
    - `distance_km` must be >= 0.0, else raises ValueError.
    - `express=True` multiplies the subtotal by 1.75.
    - A minimum charge of 5.0 always applies, regardless of the computed
      subtotal or express flag.
    - Result is rounded to 2 decimals.
    """
    if weight_kg <= 0.0:
        raise ValueError("weight_kg must be > 0")
    if distance_km < 0.0:
        raise ValueError("distance_km must be >= 0")
    subtotal = weight_kg * 2.5 + distance_km * 0.1
    if express:
        subtotal *= 1.75
    return round(max(subtotal, 5.0), 2)
